private void loadDataCustomerDariSQL() {
    cmbCustomer.removeAllItems();
    
    // Menembak fungsi asli: ambilCustomer() yang menghasilkan List<String[]>
    java.util.List<String[]> listCustomer = controller.ambilCustomer();
    for (String[] customer : listCustomer) {
        // Gabungkan ID (indeks 0) dan Nama (indeks 1) untuk ComboBox
        cmbCustomer.addItem(customer[0] + " - " + customer[1]);
    }
}

public void loadDataKendaraanDariSQL() {
    cmbKendaraan.removeAllItems();
    
    // Menembak fungsi asli: ambilKendaraan()
    java.util.List<String[]> listKendaraan = controller.ambilKendaraan();
    for (String[] kendaraan : listKendaraan) {
        // Gabungkan ID/Plat (indeks 0) dan Merek (indeks 1)
        cmbKendaraan.addItem(kendaraan[0] + " - " + kendaraan[2]);
    }
}

private void loadDataTransaksiKeTable() {
    javax.swing.table.DefaultTableModel modelTabel = (javax.swing.table.DefaultTableModel) jTable1.getModel();
    modelTabel.setRowCount(0); // Bersihkan baris tabel agar tidak menumpuk
    
    //Menembak fungsi asli: ambilTabelTransaksi()
    java.util.List<String[]> listTransaksi = controller.ambilTabelTransaksi();
    for (String[] barisData : listTransaksi) {
        // Masukkan data array String secara utuh sebagai baris tabel baru
        modelTabel.addRow(barisData);
    }
}
    
