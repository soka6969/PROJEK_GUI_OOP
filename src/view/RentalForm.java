    private void cmbKendaraanItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbKendaraanItemStateChanged
        // TODO add your handling code here:
        if (evt.getStateChange() == java.awt.event.ItemEvent.SELECTED && cmbKendaraan.getSelectedItem() != null) {
        try {
            // 1. Ambil ID kendaraan dari teks ComboBox 
            String idTerpilih = cmbKendaraan.getSelectedItem().toString().split(" - ")[0];
            
            // 2. Minta daftar data kendaraan asli dari Controller
            java.util.List<String[]> listKendaraan = controller.ambilKendaraan();
            
            for (String[] kendaraan : listKendaraan) {
        if (kendaraan[0].equals(idTerpilih)) {
            txtMerekKendaraan.setText(kendaraan[1]); 
            txtHargaSewa.setText(kendaraan[3]);         
            updateOtomatisTotalBayar();
            break;
                }
            }
        } catch (Exception e) {
            System.out.println("Gagal memuat detail kendaraan: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_cmbKendaraanItemStateChanged

