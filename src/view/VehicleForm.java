    private void jButtonSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSimpanActionPerformed
        // TODO add your handling code here:
//        Integer id_kendaraan = Integer.valueOf(jTextFieldIdKendaraan.getText());
        String no_plat = jTextFieldNoPlat.getText();
        String merek = jTextFieldMerek.getText();
        int harga_sewa = 0;
        if (!jTextFieldHargaSewa.getText().trim().matches("-?\\d+")) {
            JOptionPane.showMessageDialog(this, "Inputan Harga Sewa Harus Angka! ", "Input Error", JOptionPane.ERROR_MESSAGE);
            return;
        } else {
            harga_sewa = Integer.parseInt(jTextFieldHargaSewa.getText());
        }
        String tipe_kendaraan = jComboBoxTipeKendaraan.getSelectedItem().toString();
        
        String status_kendaraan = jComboBoxStatus.getSelectedItem().toString();

        if (no_plat.isEmpty() || merek.isEmpty() || jTextFieldHargaSewa.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Data Kendaraan Tidak Boleh Kosong! ", "Input Error", JOptionPane.ERROR_MESSAGE);
        } else {
            Vehicle vehicle = new Vehicle(no_plat, merek, tipe_kendaraan, harga_sewa, status_kendaraan);
            try {
                //kenali apakah update atau data baru, bisa dilihat dari selectedNIM
                boolean res;
                if (selectedIDVehicle != null) {
                    //jika selectedNIm tidak Null
                    res = controller.updateKendaraan(vehicle, selectedIDVehicle);
                } else {
                    res = controller.simpanKendaraan(vehicle);
                }
                if (res) {
                    JOptionPane.showMessageDialog(this, "Data Kendaraan Berhasil Ditambahkan! ", "Success", JOptionPane.INFORMATION_MESSAGE);
                    this.selectedIDVehicle = null;
                    this.clearForm();
                    loadVehicleData();
                } else {
                    JOptionPane.showMessageDialog(this, "Terjadi Kesalahan Saat Menambahkan Data Kendaraan! ", "Error", JOptionPane.ERROR_MESSAGE);
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButtonSimpanActionPerformed

