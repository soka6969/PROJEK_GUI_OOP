    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        // TODO add your handling code here:
        int confirm = JOptionPane.showConfirmDialog(this,
                "Apakah Anda Yakin Untuk Menghapus Data Kendaraan?",
                "Konfirmasi Hapus",
                JOptionPane.YES_NO_OPTION
        );

        if (confirm == JOptionPane.YES_OPTION) {
            // Proses delete
            try {
                controller.hapusKendaraan(selectedIDVehicle);
                JOptionPane.showMessageDialog(this, "Data Kendaraan Berhasil Dihapus!");
                this.selectedIDVehicle = null;
                this.clearForm();
                loadVehicleData();
            } catch (SQLException ex) {
                Logger.getLogger(VehicleForm.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "Terjadi Kesalahan Saat Menghapus Data Kendaraan!");
            }
        }
    }//GEN-LAST:event_jButtonDeleteActionPerformed

    private void jTableVehicleMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableVehicleMouseClicked
        // TODO add your handling code here:
        int selectedRow = jTableVehicle.getSelectedRow();
        if (selectedRow != -1) {
            jTextFieldIdKendaraan.setEditable(false);
            selectedIDVehicle = Integer.valueOf(jTableVehicle.getValueAt(selectedRow, 0).toString());
            jTextFieldIdKendaraan.setText(jTableVehicle.getValueAt(selectedRow, 0).toString());
            jTextFieldNoPlat.setText(jTableVehicle.getValueAt(selectedRow, 1).toString());
            jTextFieldMerek.setText(jTableVehicle.getValueAt(selectedRow, 2).toString());
            jComboBoxTipeKendaraan.setSelectedItem(jTableVehicle.getValueAt(selectedRow, 3).toString());
            jTextFieldHargaSewa.setText(jTableVehicle.getValueAt(selectedRow, 4).toString());
            jComboBoxStatus.setSelectedItem(jTableVehicle.getValueAt(selectedRow, 5).toString());
        }
    }//GEN-LAST:event_jTableVehicleMouseClicked

